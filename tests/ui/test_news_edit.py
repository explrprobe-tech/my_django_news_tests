import pytest
from playwright.sync_api import Page, expect

def test_news_edit_editor_user(page: Page, base_url: str, editor_user, admin_session):
    """Editor user can see news edit page"""
    import re
    page.goto(base_url)
    page.get_by_role("link", name="🔐 Войти").click()
    page.locator("#id_username").fill(editor_user["username"])
    page.locator("#id_password").fill(editor_user["password"])
    page.get_by_role("button", name="Войти").click()
    page.get_by_role("link", name="📰 Читать все новости").click()
    page.locator(".edit-news-btn").nth(0).click()
    expect(page).to_have_url(re.compile(rf"{base_url}news/\d+/edit/"))
    expect(page.locator(".container .edit-news-container h1")).to_contain_text("✏️ Редактировать новость")
    expect(page.locator('.form-group label[for="id_title"]')).to_contain_text("Заголовок новости")
    expect(page.locator("#id_title")).to_be_visible()
    expect(page.locator("#id_title")).not_to_be_empty()
    expect(page.locator('.form-group label[for="id_category"]')).to_contain_text("Категория")
    expect(page.locator("#id_category")).to_be_visible()
    expect(page.locator("#id_category")).not_to_be_empty()
    expect(page.locator('.form-group label[for="id_short_description"]')).to_contain_text("Краткое описание")
    expect(page.locator("#id_short_description")).to_be_visible()
    expect(page.locator("#id_short_description")).not_to_be_empty()
    expect(page.locator(".hint-text")).to_contain_text("Это описание будет отображаться в списке новостей")
    expect(page.locator(".current-image p")).to_contain_text("📸 Текущее изображение:")
    expect(page.get_by_role("checkbox", name="Удалить изображение")).to_be_visible()
    expect(page.locator('.form-group label[for="id_photo"]')).to_contain_text("Новое изображение (необязательно)")
    expect(page.locator("#id_photo")).to_be_visible()
    expect(page.locator('.form-group:has(label[for="id_photo"]) .char-counter')).to_contain_text("Поддерживаются форматы: JPG, PNG, GIF")
    expect(page.locator('.form-group label[for="id_content"]')).to_contain_text("Содержание")
    expect(page.locator("#id_content")).to_be_visible()
    expect(page.get_by_role("checkbox", name="Опубликовано")).to_be_visible()
    expect(page.get_by_role("button", name="💾 Сохранить изменения")).to_be_visible()
    expect(page.get_by_role("link", name="❌ Отмена")).to_be_visible()
