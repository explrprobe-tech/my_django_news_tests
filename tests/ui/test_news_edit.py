def test_news_edit_edition(page: Page, base_url: str, test_news, test_category, editor_user):
    """Editor user can edit news on news edit page"""
    page.goto(base_url)
    page.get_by_role("link", name="🔐 Войти").click()
    page.locator("#id_username").fill(editor_user["username"])
    page.locator("#id_password").fill(editor_user["password"])
    page.get_by_role("button", name="Войти").click()
    test_news_url = test_news["test_news_url"]
    page.goto(f"{test_news_url}edit/")
    page.locator("#id_title").fill("Changed title")
    page.locator("#id_category").select_option(test_category["test_category_id"])
    page.get_by_role("checkbox", name="Удалить изображение").click()
    page.locator("#id_content").fill("Changed content")
    page.get_by_role("checkbox", name="Опубликовано").click()
    page.get_by_role("button", name="💾 Сохранить изменения").click()
    expect(page).to_have_url(test_news_url)
    expect(page.locator(".news-detail-container .news-detail-title")).to_contain_text("Changed title")
    expect(page.locator(".news-detail-meta .category-badge")).to_contain_text(test_category["test_category_data"]["title"])
    expect(page.locator(".news-detail-content .news-detail-text")).to_contain_text("Changed content")
    expect(page.locator(".news-detail-content .news-detail-image")).not_to_be_visible()
